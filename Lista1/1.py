n1 = float(input("Insira o primeiro valor para calcular a média aritmética: \n "))
n2 = float(input("Insira o segundo valor para calcular a média aritmética: \n "))
n3 = float(input("Insira o terceiro valor para calcular a média aritmética: \n "))

print(f'A média aritmética entre {n1}, {n2} e {n3} é: {round((n1 + n2 + n3) / 3, 2)}')
